//program to find out the size of each data type


#include<iostream>
#include<math.h>
using namespace std;
int main(){
char a;
int b;
float c;
double d;
cout<<"size of char is:"<<sizeof(a)<<endl;
cout<<"size of int is:"<<sizeof(b)<<endl;
cout<<"size of float is:"<<sizeof(c)<<endl;
cout<<"size of double is:"<<sizeof(d)<<endl;
return 0;
}
